package matrices;

/**
@author Alexander Johnston 
        Copyright 2019 
        A class for making matrices
 */
public class Matrix {

	protected int rows;

	protected int cols;
	
	/**
	 * @return the number of rows in this matrix
	 */
	public int getRows() {
		return rows;
	}

	/**
	 * @return the number of columns in this matrix
	 */
	public int getCols() {
		return cols;
	}
	
}
