package directedGraph;


/**
 * @author Alexander Johnston
 *         Copyright 2019
 *         A class for creating Nodes for three dimensional directed graph cycles
 */
public class ThreeDimensionalNode extends Object{

	// A node from a directed graph
	public Object node;

	/**Creates an empty directed graph node
	 * 
	 */
	public ThreeDimensionalNode() {

	}

	/**       Creates a directed graph node
	 * @param node as a node from a directed graph
	 */
	public ThreeDimensionalNode(Object node){
		this.node = node;
	}

}
